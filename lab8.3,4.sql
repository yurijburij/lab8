SELECT [Запис санаторію].[Код запису], [Запис санаторію].[Код санаторію], [Запис санаторію].[Година запису],
Санаторій.[Назва санаторію], Пацієнт.[Ім*я пацієнта]
FROM [Запис санаторію] inner join Пацієнт
on [Запис санаторію].[Код пацієнта]=Пацієнт.[Код пацієнта]
inner join Санаторій
on [Запис санаторію].[Код санаторію]=Санаторій.[Код санаторію]
ORDER BY Пацієнт.[Ім*я пацієнта]